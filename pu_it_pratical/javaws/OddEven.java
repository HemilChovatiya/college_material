import java.util.*;
class OddEven
{
   public static void main(String args[])
     {  
        int a;
        Scanner sc = new Scanner(System.in);
       
        a= sc.nextInt();
          if(a%2==0)
           {
             System.out.println(a+" is an Even Number");      
           }
           else
           {
             System.out.println(a+" is an Odd Number");      
           }
         
     }
 
}
